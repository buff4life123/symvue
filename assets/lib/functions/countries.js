function setDefault(d) {
    let c = countries.slice();
    d && d.slice().reverse().forEach((key) => {
        c.sort(function(x,y){ return x.code == key ? -1 : y.code == key ? 1 : 0; });  
    });
    return c;
}

export const number = function(d) {
    return setDefault(d);
}

export const country = function(d) {
    return setDefault(d).filter(country => !!country.name);
}

export const adjective = function(d) {
    return setDefault(d).filter(country => !!country.adjective);
}